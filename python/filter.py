def remove_duplicates_and_save(s1: str, s2: str, filename: str = "final.txt"):
    # s1, s2 파싱
    words1 = [w.strip() for w in s1.splitlines() if w.strip()]
    words2 = {w.strip() for w in s2.splitlines() if w.strip()}  # set for 빠른 비교

    # s2에 없는 단어만 s1에서 유지
    final_words = [w for w in words1 if w not in words2]

    # 파일 저장
    with open(filename, "w", encoding="utf-8") as f:
        f.write("\n".join(final_words))

    return final_words, len(final_words)

